
public class preincrement {

	public static void main(String[] args) {
		int i=10;
		
	System.out.println(++i);// Pre-increment
	
	
	int j=199;	
	System.out.println(j++);//post increment
	
	
	int k=2000;
	System.out.println(--k);//pre decrement
	
	
	int p=3000;
	System.out.println(p--);//post decrement
	}

}
